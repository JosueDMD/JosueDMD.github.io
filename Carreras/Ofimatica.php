            <p>
                La carrera de Técnico en Ofimática ofrece las competencias profesionales que permiten al estudiante realizar actividades dirigidas a gestionar hardware y software de la Ofimática gestionar información de manera local gestionar información de manera remota, diseñar y gestionar bases de datos ofimáticas y, establecer comunicación ofimática <br><br>

                El desarrollo de las competencias se logra desde una perspectiva inter y transdisciplinario a través de las actividades de aprendizaje diseñadas de manera intencionada por el docente en la planeación didáctica de los módulos, las promovidas desde la relación vertical y transversal con las asignaturas de los componentes Básico y Propedéutico, y complementadas por las actividades de los programas de apoyo sicosocial para los estudiantes, dirigidas al tratamiento de las habilidades socioemocionales relativas a la autoconciencia, autorregulación y toma de decisiones. <br><br>

                El uso constante de las Tecnologías de la Información y Comunicación no deben ser factor de deshumanización e indiferencia ante la problemática social, por el contrario, el conocimiento y la gestión de información, debe concientizar y llevar a la reflexión como agente proactivo que transforme su realidad y promueva el desarrollo humano y el bienestar social. <br><br>

                Para lograr las competencias en el estudiante, el campo profesional dispone de 1 200 horas de formación distribuidas en cinco módulos que van del segundo al sexto semestre los primeros tres módulos de la carrera técnica tienen una duración de 272 horas cada uno y los dos últimos de 192 Se sugiere que el docente aplique el 80 para actividades prácticas y el 20 en actividades teórico conceptuales, aunque la proporción puede ser ajustada considerando las características del entorno y las condiciones de cada plantel, pero sin que la práctica sea menor al 20 del total <br><br> 
