            <p>
                La carrera de Técnico en Contabilidad ofrece las competencias profesionales que permiten al estudiante aplicar las normas de información financiera que emita el Instituto Mexicano de Contadores Públicos, en el manejo del proceso contable en operaciones especiales, de costos, en la aplicación de las leyes relacionadas con las obligaciones fiscales, y en la elaboración de papeles de trabajo para auditoría; con ello podrá efectuar el análisis financiero de una organización, desarrollar una actitud de liderazgo y establecer relaciones interpersonales y con el medio ambiente; esta orientación se dará por medio del componente de formación profesional. <br><br>

                Asimismo podrá desarrollar competencias genéricas relacionadas principalmente con la participación en los procesos de comunicación en distintos contextos, la integración efectiva a los equipos de trabajo y la intervención consciente, en su comunidad en particular, en el país y en el mundo en general, todo con apego al cuidado del medio ambiente. <br><br>

                La formación profesional se inicia en el segundo semestre, con el desarrollo de las competencias para el registro de operaciones contables, registro de operaciones especiales, registro de operaciones de costos, cálculo de contribuciones y gestión e informe preliminar de auditoría, hasta el sexto semestre de la carrera técnica. <br><br>
