                Permite al técnico incorporarse al ámbito laboral en diversos sitios de inserción como: servicios de dibujo, supervisión de edificación residencial, supervisión de edificación de inmuebles comerciales y de servicios, colocación de muros falsos y aislamiento, trabajos de enyesado empastado y tiroleado, trabajos de pintura y otros cubrimientos de paredes, colocación de pisos cerámicos y azulejos, instalaciones hidrosanitarias y de gas, instalaciones eléctricas en construcciones, instalaciones de sistemas centrales de aire acondicionado y calefacción, división de terrenos, servicios de mapas, supervisión de edificación residencial, supervisión de edificación de inmuebles comerciales y de servicios.<br><br>

                Para lograr las competencias el estudiante debe de tener una formación profesional, que se inicia en el segundo semestre y se concluye en el sexto semestre, desarrollando en este lapso de tiempo las competencias profesionales que marca el programa de estudios.<br><br>
